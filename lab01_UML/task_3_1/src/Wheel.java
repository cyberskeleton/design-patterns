public class Wheel {
    String description;
    int sizeInches;

    public Wheel(String description, int sizeInches) {
        this.description = description;
        this.sizeInches = sizeInches;
    }

    public String getDescription() {
        return description;
    }

    public int getSizeInches() {
        return sizeInches;
    }
}
